package Day_2_Variables;

public class PrintVsPrintln {

    public static void main (String [] args){

        // ln is the way to make sure to start from new line
        // println breaks the line
        System.out.println("-First Line");           //print this and go to the next line
        System.out.println("-Second line");          //print this and go to the next line
        System.out.print("-First line without ln");  //print this and go to the same line
        System.out.print("-Second Line without ln"); //print this and go to the same line
        System.out.println("-Third line");           //print this and go to the next line
        System.out.print("-Third Line without ln");  //print this and go to the same line

    }
}
